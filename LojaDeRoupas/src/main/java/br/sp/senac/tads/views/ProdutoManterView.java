    public void recarregarTabela() {

        ArrayList<String[]> lstRetorno = ProdutoController.consultar();

        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();

        //Limpar a tabela
        modelo.setRowCount(0);

        for (String[] item : lstRetorno) {
            modelo.addRow(item);

        }

    }
    
   
