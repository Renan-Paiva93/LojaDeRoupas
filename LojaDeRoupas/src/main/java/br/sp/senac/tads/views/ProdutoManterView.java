    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (jTProdutos.getSelectedRow() >=0) {

            int indiceLinha = jTProdutos.getSelectedRow();
            int ID = Integer.parseInt(String.valueOf(jTProdutos.getValueAt(indiceLinha, 0)));

            if (ProdutoController.excluir(ID)) {
                JOptionPane.showMessageDialog(this, "Exclus찾o realizada com sucesso!");    

            } else {
                JOptionPane.showMessageDialog(this, "Exclus찾o realizada com sucesso!");
                
                recarregarTabela();
                
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela!");
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

